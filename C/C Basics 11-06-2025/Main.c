#include <stdio.h>  // Include the standard input/output library for printf function

/**
 * Entry point of the program.
 * Prints "Hello World!" followed by a newline to the standard output.
 *
 * @return 0 indicates successful program termination
 */
int main(void)
{
    printf("Hello World!\n");  // Output the greeting message with newline character
    return 0;                  // Return 0 to signal successful completion
}
